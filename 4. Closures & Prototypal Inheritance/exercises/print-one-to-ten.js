// using var declartion
// using concept of closure here
// print in reverse order

function print3 () {
    for (var i = 10; i >= 0; i--) {
        (function (i) {
            setTimeout(() => {
                console.log(10 - i);
            }, 1000 * i);
        })(i);
    }
}

print3();

