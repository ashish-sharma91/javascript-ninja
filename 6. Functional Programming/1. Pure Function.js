/*
    Pure functions are functions which do not touch anything outside that function
*/


// this is not a pure function as it is changing console
function a () {
    console.log('Hi');
}



////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// this is also not a pure function as it will change the array
const array = [1, 2, 3];
function b (arr) {
    arr.push(1);
}



////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// this is a pure function
const array = [1, 2, 3];
function c (arr) {
    const newArray = [...arr];
    newArray.push(1);
    return newArray;
}
